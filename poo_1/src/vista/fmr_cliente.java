    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        // TODO add your handling code here:
        int opcion =JOptionPane.showConfirmDialog(null, "Â¿Desea eliminar el registro?","opcion",JOptionPane.YES_OPTION);
        if(opcion==0){
        obj_Cliente = new Cliente();
        obj_Cliente.setId(Integer.valueOf(lbl_id.getText()));
        obj_Cliente.eliminar();
        tbl_clientes.setModel(obj_Cliente.leer());
      
        }
    }//GEN-LAST:event_btn_eliminarActionPerformed

